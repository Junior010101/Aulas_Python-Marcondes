b = 3 #Variavel "b" recebe o numero 3.
y = 7 #Variavel "y" recebe o numero 7.
b == 3 and y == 7 #Variavel "b" é igual a 3, e variavel "y" é igual a 7.
b == 3 and y != 7 #Variavel "b" é igual a 3, e variavel "y" é diferente de 7.
b != 3 and y != 7 #Variavel "b" é diferente de 3, e variavel "y" é diferente de 7.
b == 3 or y == 7 #variavel "b" é igual a 3, ou variavel "y" é igual a 7.
b != 3 or y == 7 #Variavel "b" é diferente de 3, ou variavel "y" é igual a 7.
b == 3 or y != 7 #Variavel "b" é igual a 3, ou variavel "y" é diferente de 7.
b != 3 or y != 7 #Variavel "b" é diferente de 3, ou variavel "y" é diferente de 7.
not b == 3 #Operação falsa, Variavel "b" Não é igual a 3.
not b != 3 #Operação verdadeira, Variavel "b" Não é igual a 3.
(b == 3) and (not y != 7) #Operação verdadeira, variavel "b" é igual a 3, e variavel "y" não é igual a 7.